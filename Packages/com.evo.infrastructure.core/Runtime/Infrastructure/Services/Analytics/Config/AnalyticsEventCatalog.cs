    public enum AnalyticsKeyKind
    {
        Event = 0,
        Parameter = 1
    }

    [Serializable]
    public sealed class AnalyticsKeyEntry
    {
        public AnalyticsKeyKind Kind = AnalyticsKeyKind.Event;
        public string Name;
        public string Key;
#if ODIN_INSPECTOR
        [LabelText("Tooltip Key")]
#endif
        [Tooltip("@_Project.Scripts.Editor.EvoTools.EvoToolsLocalization.Get(\"analytics.catalog.tooltip_key\", \"Localization key in EvoTools table for tooltip. Leave empty to use Comment.\")")]
        public string TooltipKey;
        [TextArea] public string Comment;
    }

    [CreateAssetMenu(fileName = "AnalyticsEventCatalog", menuName = "Project/Analytics/Event Catalog")]
